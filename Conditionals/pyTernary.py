## Used in inline conditional operations
## Syntax opposite from the other languages

n = 5 

comp = "Greater than 2" if n > 2 else "Smaller or equal to 2"
print(comp)

print("\n")

# Ternary operation can also be nested
greet = "Hello" if n > 10 else "Goodbye" if n > 5 else "Good Day!"
print(greet)